/**
 * An enumeration representing the probability levels of a risk.
 */
export enum Probability {
    /**
     * An improbable or highly unlikely occurrence.
     */
    Improbable = 1,

    /**
     * An occasional occurrence.
     */
    Ocasional,

    /**
     * A moderate or reasonable occurrence.
     */
    Moderado,

    /**
     * A frequent occurrence.
     */
    Frecuente,

    /**
     * A constant or continuous occurrence.
     */
    Constante
}
