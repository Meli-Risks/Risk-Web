/**
 * An enumeration representing the impact levels of a risk.
 */
export enum Impact {
    /**
     * A minimal or negligible impact.
     */
    Despreciable = 1,

    /**
     * A minor impact.
     */
    Menor,

    /**
     * A moderate impact.
     */
    Moderado,

    /**
     * A significant impact.
     */
    Significativo,

    /**
     * A severe or critical impact.
     */
    Severo
}
